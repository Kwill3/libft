#include "libft.h"

size_t	ft_intlen_bonus(int i)
{
	size_t	count;

	count = 1;
	if (i < 0)
	{
		i *= -1;
		count++;
	}
	while (!(i >= 0 && i <= 9))
	{
		i /= 10;
		count++;
	}
	return (count);
}
